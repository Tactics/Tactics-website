                <h4 class="gray">Historiek</h4>
                <p>
                In 2007 gaf de Vlaamse Gemeenschapscommissie het startschot voor het project ‘sociale functie in de kinderopvang’. Het doel van het project was het stimuleren van de samenwerking in de sector om zo te komen tot een gezamenlijk, gedragen en toegankelijk opnamebeleid. Zo werd er bijvoorbeeld binnen  regionale samenwerkingsverbanden – de zogenaamde regiogroepen - specifieke aandacht besteed aan het sensibiliseren van de leidinggevenden van de kinderdagverblijven rond de sociale functie van kinderopvang.
                Over alle regiogroepen heen is een systeem uitgewerkt voor het rondsturen van dringende opvangvragen. Dit project werd jaarlijks geëvalueerd en inhoudelijk bijgestuurd in afwachting van het nieuwe decreet kinderopvang.
                Het decreet houdende de organisatie van kinderopvang van baby’s en peuters dateert van 20 april 2012 en voorziet in de oprichting van een lokaal loket kinderopvang. Het  neutrale informatiepunt kreeg de volgende opdrachten mee : het coördineren van de aanvragen naar kinderopvang, het informeren van de ouders over de beschikbare opvangplaatsen, het samenwerken met de verschillende kinderopvangvoorzieningen, het informeren van de lokale besturen , de organisatoren van kinderopvang en Kind en Gezin over de vragen naar kinderopvangplaatsen. Eind 2012 werd de vzw ‘Samenwerken aan Kinderopvang Brussel’ opgericht  die de uitbouw van het lokaal loket kinderopvang Brussel op zich heeft genomen.
                In juni 2013 startte het bedrijf Tactics, in overleg met het werkveld , met de ontwikkeling van het online systeem dat vandaag voor het grote publiek toegankelijk wordt gemaakt.
