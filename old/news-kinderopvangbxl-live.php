                Het loket werd uitgewerkt door een samenwerkingsverband van verschillende opvanginitiatieven, ongeacht van het organiserend bestuur (vrije, gemeentelijke en gemeenschapsinitiatieven).
                De deelnemende kinderdagverblijven ontmoeten elkaar regelmatig om afspraken te maken over een evenwichtig opnamebeleid en om te leren van elkaars goede praktijken (wijzigingen van opvangplannen, bepalen sociaal tarief, …). Er is bewust niet gekozen voor een centrale toewijzing (top-down model). Door de sterke samenwerking van onder uit (bottom-up model) stemmen de opvanginitiatieven hun opvangbeleid op elkaar af en verloopt de communicatie naar de ouders éénduidig. Zo ontvangen alle ouders bij het begin van de maand een antwoord op hun opvangvraag. Een betere afstemming tussen vraag en aanbod zal leiden tot een optimale capaciteitsbezetting. Naar de toekomst toe zullen ook andere opvanginitiatieven als niet-inkomens gerelateerde groepsopvang en gezinsopvang worden uitgenodigd om aan te sluiten bij het Lokaal Loket Kinderopvang.
